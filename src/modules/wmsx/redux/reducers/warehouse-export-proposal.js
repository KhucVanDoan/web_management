import {
  CREATE_WAREHOUSE_EXPORT_PROPOSAL_FAILED,
  CREATE_WAREHOUSE_EXPORT_PROPOSAL_START,
  CREATE_WAREHOUSE_EXPORT_PROPOSAL_SUCCESS,
  DELETE_WAREHOUSE_EXPORT_PROPOSAL_FAILED,
  DELETE_WAREHOUSE_EXPORT_PROPOSAL_START,
  DELETE_WAREHOUSE_EXPORT_PROPOSAL_SUCCESS,
  GET_WAREHOUSE_EXPORT_PROPOSAL_DETAILS_FAILED,
  GET_WAREHOUSE_EXPORT_PROPOSAL_DETAILS_START,
  GET_WAREHOUSE_EXPORT_PROPOSAL_DETAILS_SUCCESS,
  SEARCH_WAREHOUSE_EXPORT_PROPOSAL_FAILED,
  SEARCH_WAREHOUSE_EXPORT_PROPOSAL_START,
  SEARCH_WAREHOUSE_EXPORT_PROPOSAL_SUCCESS,
  UPDATE_WAREHOUSE_EXPORT_PROPOSAL_FAILED,
  UPDATE_WAREHOUSE_EXPORT_PROPOSAL_START,
  UPDATE_WAREHOUSE_EXPORT_PROPOSAL_SUCCESS,
  CONFIRM_WAREHOUSE_EXPORT_PROPOSAL_FAILED,
  CONFIRM_WAREHOUSE_EXPORT_PROPOSAL_START,
  CONFIRM_WAREHOUSE_EXPORT_PROPOSAL_SUCCESS,
  REJECT_WAREHOUSE_EXPORT_PROPOSAL_FAILED,
  REJECT_WAREHOUSE_EXPORT_PROPOSAL_START,
  REJECT_WAREHOUSE_EXPORT_PROPOSAL_SUCCESS,
  RESET_WAREHOUSE_EXPORT_PROPOSAL_DETAILS_STATE,
  UPDATE_WAREHOUSE_EXPORT_PROPOSAL_QUANTITY_FAILED,
  UPDATE_WAREHOUSE_EXPORT_PROPOSAL_QUANTITY_START,
  UPDATE_WAREHOUSE_EXPORT_PROPOSAL_QUANTITY_SUCCESS,
  REQUEST_ITEM_CODE_FAILED,
  REQUEST_ITEM_CODE_START,
  REQUEST_ITEM_CODE_SUCCESS,
} from '~/modules/wmsx/redux/actions/warehouse-export-proposal'

const initialState = {
  isLoading: false,
  warehouseExportProposalList: [],
  warehouseExportProposalDetails: {},
  itemCode: [],
  total: null,
}

/**
 * Sample reducer
 * @param {object} state
 * @param {object} action
 * @returns
 */
export default function warehouseExportProposal(state = initialState, action) {
  switch (action.type) {
    case SEARCH_WAREHOUSE_EXPORT_PROPOSAL_START:
    case CREATE_WAREHOUSE_EXPORT_PROPOSAL_START:
    case UPDATE_WAREHOUSE_EXPORT_PROPOSAL_START:
    case UPDATE_WAREHOUSE_EXPORT_PROPOSAL_QUANTITY_START:
    case DELETE_WAREHOUSE_EXPORT_PROPOSAL_START:
    case CONFIRM_WAREHOUSE_EXPORT_PROPOSAL_START:
    case REJECT_WAREHOUSE_EXPORT_PROPOSAL_START:
    case GET_WAREHOUSE_EXPORT_PROPOSAL_DETAILS_START:
    case REQUEST_ITEM_CODE_START:
      return {
        ...state,
        isLoading: true,
      }
    case SEARCH_WAREHOUSE_EXPORT_PROPOSAL_SUCCESS:
      return {
        ...state,
        warehouseExportProposalList: action.payload.list,
        isLoading: false,
        total: action.payload.total,
      }

    case SEARCH_WAREHOUSE_EXPORT_PROPOSAL_FAILED:
      return {
        ...state,
        isLoading: false,
      }
    case CONFIRM_WAREHOUSE_EXPORT_PROPOSAL_FAILED:
    case CONFIRM_WAREHOUSE_EXPORT_PROPOSAL_SUCCESS:
    case REJECT_WAREHOUSE_EXPORT_PROPOSAL_FAILED:
    case REJECT_WAREHOUSE_EXPORT_PROPOSAL_SUCCESS:
    case CREATE_WAREHOUSE_EXPORT_PROPOSAL_SUCCESS:
    case CREATE_WAREHOUSE_EXPORT_PROPOSAL_FAILED:
    case UPDATE_WAREHOUSE_EXPORT_PROPOSAL_SUCCESS:
    case UPDATE_WAREHOUSE_EXPORT_PROPOSAL_FAILED:
    case UPDATE_WAREHOUSE_EXPORT_PROPOSAL_QUANTITY_SUCCESS:
    case UPDATE_WAREHOUSE_EXPORT_PROPOSAL_QUANTITY_FAILED:
    case DELETE_WAREHOUSE_EXPORT_PROPOSAL_SUCCESS:
    case DELETE_WAREHOUSE_EXPORT_PROPOSAL_FAILED:
    case REQUEST_ITEM_CODE_FAILED:
      return {
        ...state,
        isLoading: false,
      }
    case GET_WAREHOUSE_EXPORT_PROPOSAL_DETAILS_SUCCESS:
      return {
        ...state,
        warehouseExportProposalDetails: action.payload,
        isLoading: false,
      }
    case REQUEST_ITEM_CODE_SUCCESS:
      return {
        ...state,
        itemCode: action.payload,
        isLoading: false,
      }
    case GET_WAREHOUSE_EXPORT_PROPOSAL_DETAILS_FAILED:
      return {
        ...state,
        isLoading: false,
      }
    case RESET_WAREHOUSE_EXPORT_PROPOSAL_DETAILS_STATE:
      return {
        ...state,
        warehouseExportProposalDetails: {},
      }
    default:
      return state
  }
}
